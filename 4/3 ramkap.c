#include <stdio.h>
int main() {
	int m, n, i, j;
	scanf("%d %d", &m, &n);
	char matrica[m][n];
	for (i=0; i<m; i++) {
		for (j=0; j<n; j++) {
			if (i==0 || i==m-1 || j==0 || j==n-1) {
				matrica[i][j]='-';}
			else {
				matrica[i][j]='+';
			}
		printf("%c", matrica[i][j]);
		}
	printf("\n");
	}
return 0;	
}
